
public class Casillas {
	int posRow;
	int posCol;
	boolean mina;
	
	public int getPosRow() {
		return posRow;
	}
	public void setPosRow(int posRow) {
		this.posRow = posRow;
	}
	public int getPosCol() {
		return posCol;
	}
	public void setPosCol(int posCol) {
		this.posCol = posCol;
	}
	public boolean isMina() {
		return mina;
	}
	public void setMina(boolean mina) {
		this.mina = mina;
	}
	
}
